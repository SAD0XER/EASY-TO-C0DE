# Program to add something in an existing list.

# There are four ways of doing so >>>

# First way >>>

list = [1,2,3,4]
list.append(5)
print(list)

# Second way >>>

list = [1,2,3,5]
list.insert(3,4)
print(list)

# Third way >>>

list = [1,2,3]
list.extend([4,5])
print(list)

# Fourth way >>>

list = [1,2,3]
list+=[4,5]
print(list)

